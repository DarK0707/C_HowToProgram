#include <stdio.h>

int main() {

    int sayi1, sayi2;
    printf("Please enter 2 integer :\n");
    scanf("%d %d",&sayi1,&sayi2);

    printf("Sum of numbers are : %d\n",sayi1+sayi2);
    printf("Product of numbers are :%d\n",sayi1*sayi2);
    printf("Difference of numbers are :%d\n",sayi1-sayi2);
    printf("Quotient of numbers are :%d\n",sayi1/sayi2);
    printf("Remainder of numbers are :%d\n",sayi1%sayi2);


}